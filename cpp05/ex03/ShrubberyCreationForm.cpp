#include "ShrubberyCreationForm.hpp"

ShrubberyCreationForm::ShrubberyCreationForm(void):
	AForm("ShrubberyCreationForm", 145, 137),
	_target("ShrubberyCreationForm")
{
	std::cout << "Shrubbery Creation Form constructor called" << std::endl;
	return ;
}

ShrubberyCreationForm::ShrubberyCreationForm(std::string target):
	AForm(target, 145, 137),
	_target(target)
{
	std::cout << "Shrubbery Creation Form constructor called" << std::endl;
	return ;
}

ShrubberyCreationForm::ShrubberyCreationForm(ShrubberyCreationForm const &obj):
	AForm(obj.getTarget(), 145, 137),
	_target(obj.getTarget())
{
	std::cout << "Shrubbery Creation Form copy constructor called" << std::endl;
	return ;
}

ShrubberyCreationForm::~ShrubberyCreationForm(void)
{
	std::cout << "Shrubbery Creation Form destructor called" << std::endl;
	return ;
}

ShrubberyCreationForm&	ShrubberyCreationForm::operator=(ShrubberyCreationForm const &obj)
{
	if (this != &obj)
	{
		this->setSign(obj.getSign());
		this->_target = obj.getTarget();
	}
	return (*this);
}

std::string	ShrubberyCreationForm::getTarget(void) const
{
	return (this->_target);
}

void	ShrubberyCreationForm::execute(Bureaucrat& bureaucrat) const
{
	if (!this->getSign())
	{
		throw ShrubberyCreationForm::NotSignedException();
	}
	else if (bureaucrat.getGrade() > this->getGradeExecute())
	{
		throw ShrubberyCreationForm::GradeTooLowException();
	}
	else
	{
		this->createFile();
	}
}

